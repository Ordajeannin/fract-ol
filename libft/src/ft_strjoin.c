#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	int		l;
	char	*new;

	i = 0;
	j = 0;
	l = ft_strlen(s1) + ft_strlen(s2);
	new = (char *)malloc(sizeof(char) * (l + 1));
	if (!new)
		return (NULL);
	while (s1[i])
		new[j++] = s1[i++];
	i = 0;
	while (s2[i])
		new[j++] = s2[i++];
	new[j] = '\0';
	return (new);
}
